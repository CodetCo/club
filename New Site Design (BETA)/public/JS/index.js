$(function() {
    $(window).scroll(function() {
       // console.log('scrolling ', $(window).scrollTop(), $(document).height());
        if($(window).scrollTop() >= 200 && $(window).scrollTop() <= ($(document).height() - 500)) {
            $('#mainNav').removeClass('hide');
        } else {
            $('#mainNav').addClass('hide');
        }
    });
});


